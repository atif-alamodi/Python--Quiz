let currentQuestion = 0;
let score = 0;
let answered = false;

const questionEl = document.getElementById("question");
const answersEl = document.getElementById("answers");
const resultEl = document.getElementById("result");
const nextBtn = document.getElementById("nextBtn");

function loadQuestion() {
  answered = false;
  nextBtn.disabled = true;
  answersEl.innerHTML = "";

  const q = questions[currentQuestion];
  questionEl.innerText = q.question;

  // ุนุฑุถ ุงูุฏุฑุฌุฉ ูุจู ุงูุฅุฌุงุจุฉ (ุงุฎุชูุงุฑู)
  resultEl.innerHTML =
    "<div style='text-align:right;'>" +
    "<strong>ุงูุฏุฑุฌุฉ:</strong> " + score + " / " + questions.length +
    "</div>" +
    "<div style='text-align:right; margin-top:6px;'>ุงุฎุชุฑ ุฅุฌุงุจุฉ ูุนุฑุถ ุงูุชุตุญูุญ.</div>";

  for (let i = 0; i < q.answers.length; i++) {
    const btn = document.createElement("button");
    btn.innerText = q.answers[i];
    btn.classList.add("answer-btn");
    btn.onclick = function () {
      checkAnswer(i);
    };
    answersEl.appendChild(btn);
  }
}

function checkAnswer(selectedIndex) {
  if (answered) return;
  answered = true;

  const q = questions[currentQuestion];
  const correctIndex = q.correct;

  const buttons = document.querySelectorAll(".answer-btn");
  for (let i = 0; i < buttons.length; i++) {
    buttons[i].disabled = true;
    if (i === correctIndex) buttons[i].classList.add("correct");
    if (i === selectedIndex && i !== correctIndex) buttons[i].classList.add("wrong");
  }

  const isCorrect = (selectedIndex === correctIndex);
  if (isCorrect) score++;

  // โ ุงูุฏุฑุฌุฉ ุฃูููุง ุนูุฏ ุงูุชุตุญูุญ
  resultEl.innerHTML =
    "<div style='text-align:right; line-height:1.9;'>" +
    "<div style='font-size:18px; margin-bottom:8px;'><strong>ุงูุฏุฑุฌุฉ:</strong> " +
    score + " / " + questions.length + "</div>" +
    "<div style='margin-bottom:6px;'>" + (isCorrect ? "ุฅุฌุงุจุฉ ุตุญูุญุฉ โ" : "ุฅุฌุงุจุฉ ุฎุงุทุฆุฉ โ") + "</div>" +
    "<div><strong>ุงูุฅุฌุงุจุฉ ุงูุตุญูุญุฉ:</strong> " + q.answers[correctIndex] + "</div>" +
    "<div style='margin-top:6px;'><strong>ุงูุดุฑุญ:</strong> " + q.explanation + "</div>" +
    "</div>";

  nextBtn.disabled = false;
}

nextBtn.onclick = function () {
  if (!answered) return;

  currentQuestion++;
  if (currentQuestion < questions.length) {
    loadQuestion();
  } else {
    showFinalResult();
  }
};

function showFinalResult() {
  questionEl.innerText = "ุงูุชูู ุงูุงุฎุชุจุงุฑ ๐";
  answersEl.innerHTML = "";
  nextBtn.style.display = "none";

  const percent = Math.round((score / questions.length) * 100);

  resultEl.innerHTML =
    "<div style='text-align:center; line-height:2;'>" +
    "<h2>ุงูุฏุฑุฌุฉ ุงูููุงุฆูุฉ</h2>" +
    "<h1>" + score + " / " + questions.length + "</h1>" +
    "<h3>" + percent + "%</h3>" +
    "<button id='restartBtn' style='background:#16a34a;color:#fff;padding:10px 16px;border-radius:8px;border:none;cursor:pointer;'>ุฅุนุงุฏุฉ ุงูุงุฎุชุจุงุฑ</button>" +
    "</div>";

  document.getElementById("restartBtn").onclick = function () {
    currentQuestion = 0;
    score = 0;
    nextBtn.style.display = "inline-block";
    loadQuestion();
  };
}

